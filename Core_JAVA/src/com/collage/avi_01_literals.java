package com.collage;

public class avi_01_literals {
    public static void main(String[] args){
        System.out.println("Practice of literals");

        byte age= 18;
        System.out.println(age);

        int n=1084;
        System.out.println(n);

        char ch='A';
        System.out.println(ch);

        float F=5.6f;
        System.out.println(F);

        double D=2.453332;
        System.out.println(D);

        long V= 7824734674563L;
        System.out.println(V);

        String Q="AVISHKAR";
        System.out.println(Q);
    }
}
