package com.collage;

public class Wrap {
    public static void main(String[] args) {
        int a=10;
        Integer iobj=a;
        System.out.println(iobj);

        Double dobj=56.78;
        double x=dobj;
        System.out.println(x);

        String s=Integer.toString(1200);
        System.out.println(s);


        int q=Integer.parseInt("200");
        System.out.println(q);

    }
}
